package methods1;

public class Calculator {
	
	public int sum(int x, int y) {
		return x + y;
	}
	
	public int minus(int x, int y) {
		return x - y;
	}
	
	public int multiply(int x, int y) {
		return x * y;
	}
	
	public int division(int x, int y) {
		return x / y;
	}
	
	public float square(int x) {
		return (float)Math.pow(x, 2);
	}
}
